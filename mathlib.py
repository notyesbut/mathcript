# mathscript/stdlib/mathlib.py

import math

def sin(x):
    return math.sin(x)

def cos(x):
    return math.cos(x)

def tan(x):
    return math.tan(x)

def log(x, base=math.e):
    return math.log(x, base)

def exp(x):
    return math.exp(x)

def sqrt(x):
    return math.sqrt(x)

def pow(x, y):
    return math.pow(x, y)
